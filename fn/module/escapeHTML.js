module.exports = escapeHTML;

function escapeHTML(unsafe) 
{
    return unsafe.replace(/&amp;/gi, "&")
        .replace(/&lt;/gi, "<")
        .replace(/&gt;/gi, ">")
        .replace(/&quot;/gi, `"`)
        .replace(/&#039;/gi, `'`)
        .replace(/&Agrave;/gi, "À")
        .replace(/&Aacute;/gi, "Á")
        .replace(/&Acirc;/gi, "Â")
        .replace(/&Atilde;/gi, "Ã")
        .replace(/&Auml;/gi, "Ä")
        .replace(/&Aring;/gi, "Å")
        .replace(/&agrave;/gi, "à")
        .replace(/&aacute;/gi, "á")
        .replace(/&acirc;/gi, "â")
        .replace(/&atilde;/gi, "ã")
        .replace(/&auml;/gi, "ä")
        .replace(/&aring;/gi, "å")
        .replace(/&AElig;/gi, "Æ")
        .replace(/&aelig;/gi, "æ")
        .replace(/&szlig;/gi, "ß")
        .replace(/&Ccedil;/gi, "Ç")
        .replace(/&ccedil;/gi, "ç")
        .replace(/&Egrave;/gi, "È")
        .replace(/&Eacute;/gi, "É")
        .replace(/&Ecirc;/gi, "Ê")
        .replace(/&Euml;/gi, "Ë")
        .replace(/&egrave;/gi, "è")
        .replace(/&eacute;/gi, "é")
        .replace(/&ecirc;/gi, "ê")
        .replace(/&euml;/gi, "ë")
        .replace(/&#131;/gi, "ƒ")
        .replace(/&Igrave;/gi, "Ì")
        .replace(/&Iacute;/gi, "Í")
        .replace(/&Icirc;/gi, "Î")
        .replace(/&Iuml;/gi, "Ï")
        .replace(/&igrave;/gi, "ì")
        .replace(/&iacute;/gi, "í")
        .replace(/&icirc;/gi, "î")
        .replace(/&iuml;/gi, "ï")
        .replace(/&Ntilde;/gi, "Ñ")
        .replace(/&ntilde;/gi, "ñ")
        .replace(/&Ograve;/gi, "Ò")
        .replace(/&Oacute;/gi, "Ó")
        .replace(/&Ocirc;/gi, "Ô")  
        .replace(/&Otilde;/gi, "Õ")  
        .replace(/&Ouml;/gi, "Ö")  
        .replace(/&ograve;/gi, "ò")  
        .replace(/&oacute;/gi, "ó")  
        .replace(/&ocirc;/gi, "ô")  
        .replace(/&otilde;/gi, "õ")  
        .replace(/&ouml;/gi, "ö")  
        .replace(/&Oslash;/gi, "Ø")  
        .replace(/&oslash;/gi, "ø")  
        .replace(/&#140;/gi, "Œ")  
        .replace(/&#156;/gi, "œ")  
        .replace(/&#138;/gi, "Š")  
        .replace(/&#154;/gi, "š")  
        .replace(/&Ugrave;/gi, "Ù")  
        .replace(/&Uacute;/gi, "Ú")  
        .replace(/&Ucirc;/gi, "Û")  
        .replace(/&Uuml;/gi, "Ü")  
        .replace(/&ugrave;/gi, "ù")  
        .replace(/&uacute;/gi, "ú")  
        .replace(/&ucirc;/gi, "û")  
        .replace(/&uuml;/gi, "ü")  
        .replace(/&#181;/gi, "µ")  
        .replace(/&#215;/gi, "×")  
        .replace(/&Yacute;/gi, "Ý")  
        .replace(/&#159;/gi, "Ÿ")  
        .replace(/&yacute;/gi, "ý")  
        .replace(/&yuml;/gi, "ÿ")  
        .replace(/&#176;/gi, "°")  
        .replace(/&#134;/gi, "†")  
        .replace(/&#135;/gi, "‡")  
        .replace(/&#177;/gi, "±")  
        .replace(/&#171;/gi, "«")  
        .replace(/&#187;/gi, "»")  
        .replace(/&#191;/gi, "¿")  
        .replace(/&#161;/gi, "¡")  
        .replace(/&#183;/gi, "·")  
        .replace(/&#149;/gi, "•")  
        .replace(/&#153;/gi, "™")  
        .replace(/&copy;/gi, "©")  
        .replace(/&reg;/gi, "®")  
        .replace(/&#167;/gi, "§")  
        .replace(/&#182;/gi, "¶");
}
